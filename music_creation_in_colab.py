def make_ocean(duration_s, sr=SR, seed=3):
    rng = np.random.default_rng(seed)
    n = int(duration_s * sr)
    t = np.arange(n) / sr

    noise = rng.normal(0, 1, n).astype(np.float32)
    hiss = lowpass(noise, cutoff=2400, sr=sr, order=4)
    hiss = hiss / (np.std(hiss) + 1e-9)

    swell1 = 0.5 * (1 - np.cos(2*np.pi*(1/7.5)*t))
    swell2 = 0.5 * (1 - np.cos(2*np.pi*(1/16.0)*t))
    env = 0.20 + 0.55*(swell1**1.6) + 0.20*(swell2**1.8)

    rumble = 0.015*np.sin(2*np.pi*55*t) + 0.008*np.sin(2*np.pi*72*t)

    x = (0.20 * env * hiss + rumble).astype(np.float32)
    x = lowpass(x, cutoff=2600, sr=sr, order=4)
    x = x / (np.max(np.abs(x)) + 1e-9) * 0.95
    return x

