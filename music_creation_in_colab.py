def bt(beat):  # beats -> seconds
    return (60.0 / TEMPO) * beat

def add_note(inst, pitch, start_beat, dur_beat, vel=100):
    n = pretty_midi.Note(
        velocity=int(np.clip(vel, 1, 127)),
        pitch=int(np.clip(pitch, 0, 127)),
        start=bt(start_beat),
        end=bt(start_beat + dur_beat),
    )
    inst.notes.append(n)

def add_chord(inst, pitches, start_beat, dur_beat, vel=95, strum=0.0):
    # optional tiny strum in beats
    for i, p in enumerate(pitches):
        add_note(inst, p, start_beat + i*strum, dur_beat - i*strum, vel)

def add_drums_4_4(start_beat, bars, intensity=1.0):
    # Kick=36, Snare=38, HH closed=42, Crash=49, Ride=51, Tom=45/47
    beat = start_beat
    for _ in range(bars):
        # crash on bar start sometimes
        if random.random() < 0.35:
            add_note(drums, 49, beat, 0.25, vel=int(95*intensity))
        # 8th hats
        for i in range(8):
            add_note(drums, 42, beat + i*0.5, 0.15, vel=int((65 if i%2==0 else 55)*intensity))
        # backbeat
        add_note(drums, 38, beat + 1.0, 0.2, vel=int(100*intensity))
        add_note(drums, 38, beat + 3.0, 0.2, vel=int(100*intensity))
        # kick pattern (syncopated)
        for k in [0.0, 1.5, 2.0, 2.75]:
            add_note(drums, 36, beat + k, 0.18, vel=int(105*intensity))
        beat += 4.0

def add_drums_7_8(start_beat, bars, intensity=1.0):
    # 7/8 = 3.5 beats if beat = quarter-note
    beat = start_beat
    for _ in range(bars):
        if random.random() < 0.4:
            add_note(drums, 49, beat, 0.25, vel=int(92*intensity))
        # hats in 8ths -> 7 eighths = 3.5 beats => steps of 0.5, total 7 hits
        for i in range(7):
            add_note(drums, 42, beat + i*0.5, 0.13, vel=int((62 if i%2==0 else 52)*intensity))
        # snare accents (grouping 2+2+3 eighths)
        add_note(drums, 38, beat + 1.0, 0.2, vel=int(95*intensity))
