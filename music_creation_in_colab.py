def chord_pad(freqs, dur_s, amp=0.18):
    n = int(dur_s*SR)
    t = np.arange(n)/SR
    x = np.zeros(n, dtype=np.float32)
    for f in freqs:
        for k, a in [(1,1.0),(2,0.35),(3,0.2)]:
            x += (amp*a)*np.sin(2*np.pi*(f*k)*t)
    x = lowpass(x, cutoff=1800)
    fade = np.ones(n, dtype=np.float32)
    fi = int(0.08*n); fo = int(0.12*n)
    fade[:fi] = np.linspace(0,1,fi)
    fade[-fo:] = np.linspace(1,0,fo)
    x *= fade
    return normalize(x, 0.6)

