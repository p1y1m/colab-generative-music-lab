def pad_section(start, bars):
    beat = start
    for i in range(bars):
        chord = PROG[i % 4]["ch"]
        # pad long + gentle strum
        add_chord(pad, chord, beat, 4.0, vel=55, strum=0.03)
        # sparkle arps
        arp = chord + [chord[0]+12]
        t = beat
        for k in range(8):
            p = arp[k % len(arp)]
            add_note(plk, p, t, 0.25, vel=45)
            t += 0.5
        beat += 4.0
    return beat

def bass_section(start, bars):
    beat = start
    for i in range(bars):
        root = PROG[i % 4]["root"] - 24  # down 2 octaves
        # driving 8ths with occasional octave jump
        pattern = [0,0,7,0,5,0,7,0]  # semitone offsets (power-ish movement)
        t = beat
        for step, off in enumerate(pattern):
            p = root + off
            vel = 92 if step in [0,4] else 80
            add_note(bass, p, t, 0.45, vel=vel)
            if step in [3,7] and random.random() < 0.4:
                add_note(bass, p+12, t, 0.35, vel=70)
            t += 0.5
        beat += 4.0
    return beat

def piano_comp(start, bars):
    # rhythmic piano stabs + broken chords (keeps it “rock/electro”)
    beat = start
    for i in range(bars):
        chord = PROG[i % 4]["ch"]
        # stabs on strong beats
        for k in [0.0, 1.0, 2.0, 3.0]:
            add_chord(piano, [chord[0], chord[2], chord[3] if len(chord)>3 else chord[2]+7],
                      beat+k, 0.35, vel=92, strum=0.01)
        # broken chord run
        run = chord + [chord[0]+12]
        t = beat + 0.25
        for r in range(12):
            p = run[r % len(run)]
            add_note(piano, p, t, 0.12, vel=70)
            t += 0.25
        beat += 4.0
    return beat

def fast_piano_solo(start, bars):
    # fast 16th-note solo (A minor / A Dorian flavor), wide range
    beat = start
    scale_a = [69,71,72,74,76,77,79,81]  # A4..A5 (A dorian-ish)
    scale_b = [69,72,74,76,79,81]        # pent
    for b in range(bars):
        t = beat
        for i in range(16):  # 16ths in a bar
            src = scale_a if (b % 2 == 0) else scale_b
            p = random.choice(src) + (12 if random.random()<0.55 else 0)
            vel = 110 if i in [0,4,8,12] else 92
            add_note(piano, p, t, 0.22, vel=vel)
            # subtle synth double on accents
            if i in [0,4,8,12] and random.random()<0.7:
                add_note(lead2, p, t, 0.22, vel=60)
            t += 0.25
        # end-of-phrase high hit
        if b % 2 == 1:
            add_note(piano, 93 if random.random()<0.5 else 95, beat+3.0, 1.0, vel=120)
            add_note(lead2,  93 if random.random()<0.5 else 95, beat+3.0, 1.0, vel=62)
        beat += 4.0
    return beat

def breakdown(start, bars):
    # half-time + airy pad + piano arps
    beat = start
    for i in range(bars):
        chord = PROG[i % 4]["ch"]
        add_chord(pad, chord, beat, 4.0, vel=52, strum=0.05)
        # half-time drums
        add_note(drums, KICK, beat+0.0, 0.2, vel=95)
        add_note(drums, SNARE, beat+2.0, 0.2, vel=95)
        for h in range(8):
            add_note(drums, HHC, beat + h*0.5, 0.12, vel=40)
        # piano arpeggio (triplet-ish feel with 0.333 beats)
        arp = chord + [chord[0]+12]
        t = beat
        for k in range(12):
            p = arp[k % len(arp)]
            add_note(piano, p, t, 0.15, vel=62)
            t += (4.0/12.0)
        beat += 4.0
    return beat

def ending(start, bars):
    beat = start
    for i in range(bars):
        chord = PROG[i % 4]["ch"]
        add_chord(piano, chord, beat, 4.0, vel=95, strum=0.02)
        add_chord(pad, chord, beat, 4.0, vel=50, strum=0.05)
        # big drums
        add_note(drums, CRASH, beat, 0.25, vel=110)
        drums_4_4(beat, 1, intensity=1.15, hats="8th")
        # bass root sustain
        add_note(bass, PROG[i % 4]["root"]-24, beat, 4.0, vel=92)
        beat += 4.0
    # final ring
    add_chord(piano, [57,60,64,69,72], beat, 6.0, vel=115, strum=0.02)  # Am-ish
    add_chord(pad,   [57,60,64,69,72], beat, 6.0, vel=55,  strum=0.06)
    add_note(drums, CRASH, beat, 0.25, vel=120)
    add_note(bass, 33, beat, 6.0, vel=95)  # A1
    return beat + 6.0

