KICK=36; SNARE=38; HHC=42; HHO=46; CRASH=49; RIDE=51; CLAP=39

def drums_4_4(start, bars, intensity=1.0, hats="16th"):
    beat = start
    for b in range(bars):
        # crash at section start sometimes
        if b == 0 or random.random() < 0.18:
            add_note(drums, CRASH, beat, 0.25, vel=int(95*intensity))

        # hats
        if hats == "16th":
            for i in range(16):
                vel = int((58 if i%2==0 else 45)*intensity)
                add_note(drums, HHC, beat + i*0.25, 0.12, vel=vel)
        else:  # 8th
            for i in range(8):
                vel = int((60 if i%2==0 else 48)*intensity)
                add_note(drums, HHC, beat + i*0.5, 0.14, vel=vel)

        # snare/clap on 2 and 4
        add_note(drums, SNARE, beat + 1.0, 0.2, vel=int(102*intensity))
        add_note(drums, CLAP,  beat + 1.0, 0.2, vel=int(70*intensity))
        add_note(drums, SNARE, beat + 3.0, 0.2, vel=int(102*intensity))
        add_note(drums, CLAP,  beat + 3.0, 0.2, vel=int(70*intensity))

        # kick pattern (driving, slightly syncopated)
        for k in [0.0, 0.75, 1.5, 2.0, 2.75, 3.5]:
            add_note(drums, KICK, beat + k, 0.15, vel=int(110*intensity))

        # occasional open hat lift
        if random.random() < 0.25:
            add_note(drums, HHO, beat + 3.75, 0.2, vel=int(75*intensity))

        beat += 4.0
    return beat

