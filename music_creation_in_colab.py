def fast_solo(start_beat, bars):
    # Fast melodic solo in E Dorian / E minor pentatonic mix, 16th-note runs
    beat = start_beat
    # scales around E4-E6
    e_dorian = [64,66,67,69,71,73,74,76]  # E4..E5 dorian
    pent     = [64,67,69,71,74,76]        # E minor pent
    for b in range(bars):
        # 16ths: 16 notes per bar in 4/4
        t = beat
        for i in range(16):
            src = e_dorian if (b % 2 == 0) else pent
            p = random.choice(src) + (12 if random.random()<0.55 else 0)  # jump up octave often
            # phrase shaping
            vel = 105 if i in [0,4,8,12] else 92
            add_note(lead, p, t, 0.25, vel=vel)
            t += 0.25
        # add a held "scream" note at end of every 2 bars
        if b % 2 == 1:
            end_note = 88 if random.random()<0.5 else 90  # E6/F#6-ish
            add_note(lead, end_note, beat+3.0, 1.0, vel=118)
        beat += 4.0
    return beat

