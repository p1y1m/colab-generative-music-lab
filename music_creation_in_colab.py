SONG_MS = int(TOTAL_BARS * BAR_S * 1000)

# -------- Helpers (audio synth) --------
def lowpass(x, cutoff=8000, sr=SR, order=4):
    b, a = butter(order, cutoff/(sr/2), btype="low")
    return lfilter(b, a, x)

def highpass(x, cutoff=200, sr=SR, order=4):
    b, a = butter(order, cutoff/(sr/2), btype="high")
    return lfilter(b, a, x)

def env_exp(n, decay=0.06, sr=SR):
    t = np.arange(n)/sr
    return np.exp(-t/decay)

def sine(freq, dur_s, amp=0.6, sr=SR):
    n = int(dur_s*sr)
    t = np.arange(n)/sr
    return (amp*np.sin(2*np.pi*freq*t)).astype(np.float32)

def noise(dur_s, amp=0.4, sr=SR):
    n = int(dur_s*sr)
    return (amp*np.random.uniform(-1,1,n)).astype(np.float32)

def normalize(x, target=0.95):
    m = np.max(np.abs(x)) + 1e-9
    return (x/m*target).astype(np.float32)

def to_audiosegment_mono(x, sr=SR):
    x = np.clip(x, -1, 1)
    pcm = (x * 32767).astype(np.int16).tobytes()
    return AudioSegment(data=pcm, sample_width=2, frame_rate=sr, channels=1)

def overlay(base: AudioSegment, layer: AudioSegment, position_ms=0, gain_db=0.0):
    return base.overlay(layer + gain_db, position=position_ms)

def ms(t_s): return int(t_s*1000)

# -------- Drums --------
def kick():
    dur = 0.16
    n = int(dur*SR)
    t = np.arange(n)/SR
    f0, f1 = 110.0, 55.0
    f = f0 * (f1/f0) ** (t/dur)
    phase = 2*np.pi*np.cumsum(f)/SR
    x = np.sin(phase) * env_exp(n, decay=0.08)
    x = lowpass(x, cutoff=900)
    return normalize(x, 0.85)

def clap():
    dur = 0.12
    x = noise(dur, amp=0.9)
    x = highpass(x, cutoff=1200)
    x = x * env_exp(len(x), decay=0.04)
    return normalize(x, 0.65)

def shaker():
    dur = 0.05
    x = noise(dur, amp=0.25)
    x = highpass(x, cutoff=6000)
    x = x * env_exp(len(x), decay=0.02)
    return normalize(x, 0.45)

KICK = to_audiosegment_mono(kick())
CLAP = to_audiosegment_mono(clap())
SHAK = to_audiosegment_mono(shaker())

def midi_to_freq(m):
    return 440.0 * (2 ** ((m - 69)/12))

# C - G - Am - F
roots = [48, 43, 45, 41]
chords = [
    [60, 64, 67],
    [59, 62, 67],
    [57, 60, 64],
    [57, 60, 65],
]

def pluck(freq, dur_s, amp=0.25):
    n = int(dur_s*SR)
    t = np.arange(n)/SR
    x = (np.sin(2*np.pi*freq*t) +
         0.35*np.sin(2*np.pi*freq*2*t) +
         0.20*np.sin(2*np.pi*freq*3*t)).astype(np.float32)
    x *= env_exp(n, decay=0.06)
    x = lowpass(x, cutoff=4200)
    return normalize(x, target=0.7) * amp

def bass(freq, dur_s, amp=0.35):
    x = sine(freq, dur_s, amp=amp)
    x = lowpass(x, cutoff=220)
    x *= env_exp(len(x), decay=0.12)
    return x.astype(np.float32)

# -------- Build instrumental --------
inst = AudioSegment.silent(duration=SONG_MS, frame_rate=SR)

for bar in range(TOTAL_BARS):
    bar_start_s = bar * BAR_S
    idx = bar % 4
    chord_midis = chords[idx]
    root = roots[idx]

    inst = overlay(inst, KICK, position_ms=ms(bar_start_s + 0*BEAT_S), gain_db=-1)
    inst = overlay(inst, KICK, position_ms=ms(bar_start_s + 2*BEAT_S), gain_db=-2)

    inst = overlay(inst, CLAP, position_ms=ms(bar_start_s + 1*BEAT_S), gain_db=-4)
    inst = overlay(inst, CLAP, position_ms=ms(bar_start_s + 3*BEAT_S), gain_db=-4)

    for i in range(8):
        inst = overlay(inst, SHAK, position_ms=ms(bar_start_s + i*0.5*BEAT_S), gain_db=-12)

    b1 = to_audiosegment_mono(bass(midi_to_freq(root), 0.45, amp=0.45))
    b3 = to_audiosegment_mono(bass(midi_to_freq(root), 0.45, amp=0.42))
    inst = overlay(inst, b1, position_ms=ms(bar_start_s + 0*BEAT_S), gain_db=-2)
    inst = overlay(inst, b3, position_ms=ms(bar_start_s + 2*BEAT_S), gain_db=-2)

    freqs = [midi_to_freq(m) for m in chord_midis]
    for beat in [0.0, 1.0, 2.0, 3.0]:
        f = random.choice(freqs)
        p = to_audiosegment_mono(pluck(f, 0.18, amp=0.22))
        inst = overlay(inst, p, position_ms=ms(bar_start_s + beat*BEAT_S), gain_db=-10)

inst = inst.apply_gain(-2).fade_in(800).fade_out(1200)
inst.export(INST_WAV, format="wav")

# -------- Vocals (Edge TTS Python, no SSML) --------
async def tts_line_to_wav(text, wav_out, pitch):
    tmp_mp3 = wav_out.replace(".wav", ".mp3")
    comm = edge_tts.Communicate(text=text, voice=VOICE_NAME, rate=RATE, pitch=pitch)
    await comm.save(tmp_mp3)
    subprocess.run(["bash","-lc", f'ffmpeg -y -loglevel error -i "{tmp_mp3}" -ar {SR} -ac 1 "{wav_out}"'], check=False)

async def build_vocals_async():
    vocals = AudioSegment.silent(duration=SONG_MS, frame_rate=SR)
    start_ms = int(INTRO_BARS * BAR_S * 1000)
    line_index = 0

    for _, lines in sections:
        for line in lines:
            wav_i = os.path.join(out_dir, f"line_{line_index:02d}.wav")
            # small pitch variation for “singy” feel
            pitch = "+3Hz" if (line_index % 3 == 0) else ("-2Hz" if (line_index % 3 == 1) else PITCH_BASE)
            await tts_line_to_wav(line, wav_i, pitch=pitch)

            a = AudioSegment.from_wav(wav_i)
            if len(a) > LINE_SLOT_MS:
                a = a[:LINE_SLOT_MS]
            else:
                a = a + AudioSegment.silent(duration=(LINE_SLOT_MS - len(a)), frame_rate=SR)

            pos = start_ms + line_index * LINE_SLOT_MS
            vocals = vocals.overlay(a - 1, position=pos)
            line_index += 1

    # light cleanup
    vocals_raw = os.path.join(out_dir, "vocals_raw.wav")
    vocals.export(vocals_raw, format="wav")
    vocals_fx = os.path.join(out_dir, "vocals_fx.wav")
    subprocess.run(["bash","-lc",
        f'ffmpeg -y -loglevel error -i "{vocals_raw}" '
        f'-af "highpass=f=120, lowpass=f=6500, acompressor=threshold=-18dB:ratio=2.5:attack=10:release=120" '
        f'"{vocals_fx}"'
    ], check=False)
    return AudioSegment.from_wav(vocals_fx)

# Run async safely inside notebook (nest_asyncio makes this re-entrant)
loop = asyncio.get_event_loop()
vocals = loop.run_until_complete(build_vocals_async())

# -------- Final mix --------
instrumental = AudioSegment.from_wav(INST_WAV)
final = instrumental.overlay(vocals, position=0).fade_in(800).fade_out(1200)

final.export(FINAL_WAV, format="wav")
subprocess.run(["bash","-lc", f'ffmpeg -y -loglevel error -i "{FINAL_WAV}" -codec:a libmp3lame -qscale:a 2 "{FINAL_MP3}"'], check=False)

