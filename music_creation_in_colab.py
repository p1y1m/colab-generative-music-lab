# --------- Style ----------
STYLE = "mozart"   # "mozart" (piano + alberti) or "vivaldi" (violin + strings pulse)
SEED = 7
random.seed(SEED)

# --------- Global setup ----------
TONIC = "C"
MODE  = "major"
BPM   = 132 if STYLE == "vivaldi" else 124
TIME_SIGNATURE = "4/4"
BARS = 32

k = key.Key(TONIC, MODE)
sc = scale.MajorScale(k.tonic)

# Common upbeat progressions (original sequencing)
PROG = [
    ("I", "V", "vi", "IV"),
    ("I", "IV", "V", "I"),
    ("ii", "V", "I", "I"),
    ("I", "V", "I", "V"),
]

ROMAN_TO_DEGREES = {
    "I":  [1,3,5],
    "ii": [2,4,6],
    "IV": [4,6,1],
    "V":  [5,7,2],
    "vi": [6,1,3],
}

def scale_pitch(deg:int, octave:int):
    # deg 1..7, safe always
    deg = ((deg - 1) % 7) + 1
    p = sc.pitchFromDegree(deg)
    p.octave = octave
    return p

def triad_pitches(roman:str, octave:int):
    degs = ROMAN_TO_DEGREES[roman]
    return [scale_pitch(d, octave) for d in degs]

def flatten_progression(bars:int):
    seq = []
    while len(seq) < bars:
        for group in PROG:
            seq.extend(group)
            if len(seq) >= bars:
                break
    # end with cadence V -> I
    if bars >= 2:
        seq[-2] = "V"
        seq[-1] = "I"
    return seq[:bars]

roman_seq = flatten_progression(BARS)

# --------- Accompaniment ----------
def add_alberti_bass(part, roman_seq):
    # Alberti: low-high-mid-high as eighth notes
    for roman in roman_seq:
        tps = triad_pitches(roman, octave=3)
        low, mid, high = tps[0], tps[1], tps[2]
        pattern = [low, high, mid, high] * 2
        for p in pattern:
            n = note.Note(p)
            n.quarterLength = 0.5
            n.volume.velocity = 52
            part.append(n)

def add_string_pulse(part, roman_seq):
    # Vivaldi-like pulse: 8ths on chord tones
    for roman in roman_seq:
        tps = triad_pitches(roman, octave=4)
        seq = [tps[0], tps[2], tps[1], tps[2]] * 2
        for p in seq:
            n = note.Note(p)
            n.quarterLength = 0.5
            n.volume.velocity = 60
            part.append(n)

# --------- Melody (robust, always in-scale) ----------
def add_melody(part, roman_seq):
    # Rule: strong beats = chord tones; weak beats = neighbor/passing
    for i, roman in enumerate(roman_seq):
        tps = triad_pitches(roman, octave=5)
        chord_degs = ROMAN_TO_DEGREES[roman]  # 3 degrees

        if STYLE == "vivaldi":
            # 8 eighth-notes, bright arpeggio pattern with tiny variations
            base = [tps[0], tps[2], tps[1], tps[2], tps[0], tps[2], tps[1], tps[2]]
            if i % 4 == 2:
                # small sequence move using scale degrees (still safe)
                d = chord_degs[0]
                base[1] = scale_pitch(d+1, 5)
                base[5] = scale_pitch(d+2, 5)
            for p in base:
                n = note.Note(p)
                n.quarterLength = 0.5
                n.volume.velocity = 82
                part.append(n)
        else:
            # 4 quarter-notes: chord tone, neighbor, chord tone, cadence-ish
            d0 = chord_degs[0]
            d2 = chord_degs[2]
            neighbor = d0 + (1 if i % 2 == 0 else -1)

            motif = [
                scale_pitch(d0, 5),
                scale_pitch(neighbor, 5),
                scale_pitch(d2, 5),
                scale_pitch(d0, 5),
            ]

            # occasional upbeat jump
            if i % 8 == 4:
                motif[2] = scale_pitch(d2+2, 5)

            for p in motif:
                n = note.Note(p)
                n.quarterLength = 1.0
                n.volume.velocity = 78
                part.append(n)

# --------- Build score ----------
score = stream.Score()
score.append(tempo.MetronomeMark(number=BPM))
score.append(meter.TimeSignature(TIME_SIGNATURE))
score.append(k)

mel = stream.Part()
acc = stream.Part()

if STYLE == "vivaldi":
    mel.insert(0, instrument.Violin())
    acc.insert(0, instrument.StringEnsemble())
else:
    mel.insert(0, instrument.Piano())
    acc.insert(0, instrument.Piano())

mel.append(dynamics.Dynamic('mf'))
acc.append(dynamics.Dynamic('mp'))

add_melody(mel, roman_seq)
if STYLE == "vivaldi":
    add_string_pulse(acc, roman_seq)
else:
    add_alberti_bass(acc, roman_seq)

score.insert(0, mel)
score.insert(0, acc)

